N = float(input("Введите угол в градусах (0 ≤ N < 360): "))
hours = int(N // 30)  # 360 градусов / 12 часов = 30 градусов на час
minutes = int((N % 30) / 0.5)  # 30 градусов / 60 минут = 0.5 градуса на минуту

print(f"Прошло {hours} полных часов и {minutes} полных минут с начала суток.")

